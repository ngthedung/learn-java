    private void actionKeyUprowForTable() {
        if (table != null && table.getSelectedRow() >= 1) {
            table.setRowSelectionInterval(rowSelected - 1, rowSelected - 1);
            rowSelected = rowSelected - 1;
            /**điều khiển thanh cuộn*/
            table.scrollRectToVisible(table.getCellRect(rowSelected, 0, true));
            table.updateUI();
        }
    }

    private void actionKeyDownrowForTable() {
        if (table != null && rowSelected < table.getRowCount() - 1 && rowSelected >= 0) {
            table.setRowSelectionInterval(rowSelected + 1, rowSelected + 1);
            rowSelected = rowSelected + 1;
            /**điều khiển thanh cuộn*/
            table.scrollRectToVisible(table.getCellRect(rowSelected, 0, true));
            table.updateUI();
        }
    }

